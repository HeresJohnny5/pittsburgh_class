def basic_addition(num1, num2)
  total = num1 + num2
  "#{num1} + #{num2} = #{total}"
end

def basic_subtraction(num1, num2)
  total = num1 - num2
  "#{num1} - #{num2} = #{total}"
end

def basic_multiplication(num1, num2)
  total = num1 * num2
  "#{num1} * #{num2} = #{total}"
end

def basic_division(num1, num2)
  total = num1/num2
  "#{num1} / #{num2} = #{total}"
end
